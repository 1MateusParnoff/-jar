public class exercicios05 {
    public static void main(String[] args) {

        int valor1 = 15;
        int valor2 = 8;

        int multi = valor1 * valor2;
        int soma = valor1 + valor2;
        int divide = valor1 / valor2;
        int subtrair = valor1 - valor2;

        System.out.println("A Multiplicação dos valores é: " + multi);
        System.out.println("A Divisão dos valores é: " + divide); // Na duvida usar double, para usar número com virgula
        System.out.println("A Subtração dos valores é: " + subtrair);
        System.out.println("A Soma dos valores é: " + soma);


    }
}
